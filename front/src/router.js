import Vue from 'vue'
import Router from 'vue-router'

Vue.use(Router)

export default new Router({
  mode: 'history',
  base: process.env.BASE_URL,
  routes: [
    {
      path: '/',
      component: () => import('@/views/dashboard/Index'),
      children: [
        {
          name: 'Medications',
          path: 'medications',
          component: () => import('@/views/pages/main/MedicationsTable'),
        },
        {
          name: 'Prescriptions',
          path: 'prescriptions',
          component: () => import('@/views/pages/main/PrescriptionsTable'),
        },
      ],
    },
    {
      path: '*',
      component: () => import('@/views/pages/Index'),
      children: [
        {
          name: '404 Error',
          path: '',
          component: () => import('@/views/pages/Error'),
        },
      ],
    },
  ],
})
